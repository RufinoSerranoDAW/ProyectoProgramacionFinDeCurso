    private void btn_crear_tablaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crear_tablaActionPerformed
        // TODO add your handling code here:
            Connection con= null;
            servidor= Main.getIs().getServidor();
            puerto= Main.getIs().getPuerto();
            baseDeDatos= miPadre.getBaseDeDatos();
            usuario= Main.getIs().getUsuario();
            contrasena= Main.getIs().getContrasena();
            tabla= txf_nombre_tabla.getText();
            
            String listaCampos= "";

            try {
                Con_RealizarConexion rc= new Con_RealizarConexion();
                con= rc.hazConexionBBDD(servidor, puerto, baseDeDatos, usuario, contrasena);
                Statement st = con.createStatement();
                
                for (int i = 0; i < dlmCampos.size(); i++) {
                    if (i == dlmCampos.size()-1) {
                        listaCampos= listaCampos + dlmCampos.getElementAt(i);
                    } else {
                        listaCampos= listaCampos + dlmCampos.getElementAt(i) + ", ";
                    }
                    
                }
                
                // Creación de la tabla:
                st.executeUpdate(
                    "create table `" + baseDeDatos + "`.`" + tabla + "` " +
                    "(" + listaCampos + ");");


                // Inserción de valores:
    //            String nombres[]= {"Juan", "Pedro", "Antonio"};
    //            String direcciones[]= {"C/ Falsa 123", "C/ Algo 44", "C/ Cristiano Ronaldo Suuuuuuu"};
    //            String telefonos[]= {"123", "456", "789"};
    //
    //            for (int i= 0; i < nombres.length; i++) {
    //                st.executeUpdate(
    //                    "insert into contacto (nombre, direccion, telefono) values " +
    //                    "('"+nombres[i]+"', '"+direcciones[i]+"', '"+telefonos[i]+"')");
    //            }
            } catch (SQLException ex) {
                if (ex.getErrorCode() == 1050) {
                    JOptionPane.showMessageDialog(null, "Error: la tabla '" + tabla + "' ya existe." + "\n" +
                        "Considere elegir otro nombre.", "Error al crear la tabla", 2);
                }
                if (ex.getErrorCode() == 1064) {
                    JOptionPane.showMessageDialog(null, "Error: ha ocurrido un error de sintaxis." + "\n" + 
                       "Considere comprobar los campos almacenados.", "Error de sintaxis", 2);
                    System.out.println("create table `" + baseDeDatos + "`.`" + tabla + "` " +
                    "(" + listaCampos + ");");
                }
                if (ex.getErrorCode() == 1067) {
                    JOptionPane.showMessageDialog(null, "Error: uno o más campos tienen un valor por defecto no válido." + "\n" + 
                       "Considere comprobar los campos almacenados.", "Error de sintaxis", 2);
                }
                System.out.println(ex.getMessage() + ", " + ex.getErrorCode());
            }
        
    }//GEN-LAST:event_btn_crear_tablaActionPerformed

