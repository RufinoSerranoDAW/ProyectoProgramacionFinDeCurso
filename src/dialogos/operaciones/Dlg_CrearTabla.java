    private void jcb_tipo_campoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcb_tipo_campoItemStateChanged
        // TODO add your handling code here:
        tipoCampo= (String)jcb_tipo_campo.getSelectedItem();
        switch (tipoCampo) {
            case "INT":
                spn_longitud.setEnabled(false);
                txf_valor.setText("0");
                break;
            case "FLOAT":
                spn_longitud.setEnabled(false);
                txf_valor.setText("0");
                break;
            case "DOUBLE":
                spn_longitud.setEnabled(false);
                txf_valor.setText("0");
                break;
            case "DECIMAL":
                spn_longitud.setEnabled(true);
                txf_valor.setText("0");
                break;
            case "VARCHAR":
                spn_longitud.setEnabled(true);
                txf_valor.setText("NULL");
                break;
            case "CHAR":
                spn_longitud.setEnabled(false);
                txf_valor.setText("NULL");
                break;
            case "BINARY":
                spn_longitud.setEnabled(true);
                txf_valor.setText("0");
                break;
        }
    }//GEN-LAST:event_jcb_tipo_campoItemStateChanged

    private void btn_anadir_campoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anadir_campoActionPerformed
        // TODO add your handling code here:
        String nombre= txf_nombre_campo.getText();
        String tipo= (String)jcb_tipo_campo.getSelectedItem();
        Integer longitud= (Integer)spn_longitud.getValue();
        String valor= txf_valor.getText();
        boolean pk, notnull, ai;
        
        Tabla tb;
        
        if (jcb_tipo_campo.getSelectedItem().equals("Elige un tipo")) {
            System.out.println("no implementado");
        } else {
            tb= null;
            
            if (ckb_pk.isSelected() == true) {
                pk= true;
            } else {
                pk= false;
            }
            if (ckb_null.isSelected() == true) {
                notnull= true;
            } else {
                notnull= false;
            }
            if (ckb_ai.isSelected() == true) {
                ai= true;
            } else {
                ai= false;
            }
            
            if (tipo.equals("INT")) {
                longitud= 11;
                tb= new Tabla(nombre, tipo, valor, pk, notnull, ai);
            }
            //tb= new Tabla(nombre, tipo, longitud, valor, pk, notnull, ai);
            dlmCampos.addElement(tb);
        }
    }//GEN-LAST:event_btn_anadir_campoActionPerformed

