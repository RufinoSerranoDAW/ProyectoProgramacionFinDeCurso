    private void lst_camposMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_camposMouseClicked
        // TODO add your handling code here:
        Tabla tb= (Tabla)dlmCampos.getElementAt(lst_campos.getSelectedIndex());
        txf_nombre_campo.setText(tb.getNombre());
        jcb_tipo_campo.setSelectedItem(tb.getTipo());
        if (tb.getLongitud() != null) {
            spn_longitud.setValue(tb.getLongitud());
        }
        txf_valor.setText(tb.getValor());
        if (tb.isPk() == true) {
            ckb_pk.setSelected(true);
        } else {
            ckb_pk.setSelected(false);
        }
        if (tb.isNotnull() == true) {
            ckb_null.setSelected(true);
        } else {
            ckb_null.setSelected(false);
        }
        if (tb.isAi() == true) {
            ckb_ai.setSelected(true);
        } else {
            ckb_ai.setSelected(false);
        }
    }//GEN-LAST:event_lst_camposMouseClicked

