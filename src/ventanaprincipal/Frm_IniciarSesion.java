    private void btn_iniciar_sesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_iniciar_sesionActionPerformed
        // TODO add your handling code here:
        //direccionDB= "jdbc:mysql://localhost:3306/" + txf_base_de_datos.getText() + "?useSSL=false";
        Connection con= null;
        if (txf_usuario.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Error: el campo de 'usuario' no puede quedarse vacío.", "Error al iniciar sesión", 2);
        } else {
            servidor= txf_servidor.getText();
            puerto= Integer.parseInt(txf_puerto.getText());
            usuario= txf_usuario.getText();
            contrasena= pwf_contrasena.getText();
            
            try {
                Con_RealizarConexion rc= new Con_RealizarConexion();
                con= rc.hazConexionNoBBDD(servidor, puerto, usuario, contrasena);
                
                if (clip != null) {
                    clip.stop();
                }

                JOptionPane.showMessageDialog(null, "Datos correctos. Bienvenido.", "Inicio de sesión correcto", 1);

                Dlg_Acciones ccbd= new Dlg_Acciones(this, true);
                ccbd.setVisible(true);
            } catch (SQLException ex) {
                if (ex.getErrorCode() == 0) {
                    JOptionPane.showMessageDialog(null, "Error: no se ha encontrado el servidor '" + servidor + "' o no se ha podido conectar a través del puerto '" + puerto + "'.", "Error al conectarse al servidor", 2);
                }
                if (ex.getErrorCode() == 1045) {
                    JOptionPane.showMessageDialog(null, "Error: el usuario '" + usuario + "' no existe o la contraseña introducida no coincide con los datos.", "Error al iniciar sesión", 2);
                }
                if (ex.getErrorCode() == 1130) {
                    JOptionPane.showMessageDialog(null, "Error: el servidor '" + servidor + "' ha rechazado la conexión." + "\n" +
                        "Considere comprobar si el usuario '" + usuario +"' tiene acceso a este servidor de Bases de Datos.", "Error al conectarse al servidor", 2);
                }
            } finally {
                try {
                    if (con != null) {
                        con.close();
                    }
                } catch (SQLException ex) {
                    System.out.println(ex.getMessage());
                }
            }

            }    
    }//GEN-LAST:event_btn_iniciar_sesionActionPerformed

