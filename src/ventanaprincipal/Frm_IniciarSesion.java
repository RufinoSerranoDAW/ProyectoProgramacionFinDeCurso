    private void img_woodmanMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_img_woodmanMousePressed
        // TODO add your handling code here:
        int respuesta= JOptionPane.showConfirmDialog(null, "¿Desea escuchar Mega Man 2 - Wood Man Stage (Sega Genesis Remix)?" + "\n" +
            "Podrá salir del programa aunque el audio comience a sonar.", "Reproducción de audio", JOptionPane.YES_NO_OPTION, 3);
        
        if (respuesta == JOptionPane.YES_OPTION) {
            try {
                if (clip != null) {
                    clip.stop();
                }
                
                File wood= new File("src/audio/woodman.wav");
                
                clip= AudioSystem.getClip();
                clip.open(AudioSystem.getAudioInputStream(wood));
                clip.start();

                //Thread.sleep(clip.getMicrosecondLength()/1000);
            } catch (Exception ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_img_woodmanMousePressed

